#include"Salary.h"
#include<iostream>
template <class T> T Salary<T>::delivererSalary(T* t)
{
    return (T) * t = (*t) * 2;
}
template <class T> T Salary<T>::managerSalary(T number)
{
    return (T)(number * number);
}
template <class T> T Salary<T>::postmanSalary(T t1, T t2)
{
    return (T)(t1 / t2);
}